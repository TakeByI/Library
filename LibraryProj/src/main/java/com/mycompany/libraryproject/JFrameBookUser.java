    private void jButtonShowActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonShowActionPerformed
        //выводит список книг, которые взял человек, выбранный в комбобоксе(список людей)
        lineForDelete = jComboUserBookList2.getSelectedItem().toString();
        strTokenizer = new StringTokenizer(lineForDelete, ".");
        idDelete = strTokenizer.nextToken();
        try {
            resultSet2 = statement.executeQuery("SELECT book_id, book_name FROM booklist WHERE book_id IN (SELECT fk_book_id FROM uoblist WHERE fk_user_id = " + idDelete + ")");
            defList = new DefaultListModel();
            while (resultSet2.next()) {
                defList.addElement(resultSet2.getInt("book_id") + "." + resultSet2.getString("book_name"));
            }
            jListUserBook.setModel(defList);
            resultSet2.close();
        } catch (SQLException ex) {
            Logger.getLogger(JFrameBookUser.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButtonShowActionPerformed

