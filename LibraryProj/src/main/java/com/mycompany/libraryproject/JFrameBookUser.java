    private void jButtonPassBookActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonPassBookActionPerformed
        //метод для сдачи книжки конкретным пользователем
        try {
            lineForDelete = jListUserBook.getSelectedValue();
            strTokenizer = new StringTokenizer(lineForDelete, ".");
            idDelete = strTokenizer.nextToken();
            statement.executeUpdate("DELETE FROM uoblist WHERE fk_book_id = " + idDelete); //удаляем из таблицы (человек - взятая книга) строку с книгой, которую мы сдаем
            statement.executeUpdate("UPDATE booklist SET book_in_lib = true WHERE book_id = " + idDelete); //меняем поле book_in_lib(книга в библиотеке?) на true, так как книгу сдаем обратно в библиотеку
            defList.remove(jListUserBook.getSelectedIndex());
        } catch (SQLException ex) {
            Logger.getLogger(JFrameBookUser.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButtonPassBookActionPerformed

